import 'dart:io';

/// Generate pagination files for home page
///
/// Creates pagination markdown files in content/page/{N}/index.md
/// for paginating through all blog posts on the home page.
///
/// Usage: dart run bin/generate_home_pagination.dart
void main() async {
  print('🔄 Generating home page pagination files...');

  // Configuration
  const postsPerPage = 10; // Must match postsPerPage in constants.dart
  const pageDir = 'content/page';

  // Find all published posts
  final postsDir = Directory('content/posts');
  if (!await postsDir.exists()) {
    print('❌ Error: content/posts directory not found');
    exit(1);
  }

  // Count total posts (excluding pagination pages and index.md files)
  final allFiles = await postsDir
      .list(recursive: true)
      .where((entity) => entity is File && entity.path.endsWith('.md'))
      .toList();

  // Filter out pagination pages (files in /page/ directories) and index.md files
  final posts = allFiles.where((file) => !file.path.contains('/page/') && !file.path.endsWith('/index.md')).toList();

  final totalPosts = posts.length;
  final totalPages = (totalPosts / postsPerPage).ceil();

  print('📊 Total posts: $totalPosts');
  print('📄 Pages needed: $totalPages');
  print('📝 Posts per page: $postsPerPage');

  if (totalPages <= 1) {
    print('✅ Only 1 page needed - no pagination files required');
    return;
  }

  // Clean up existing pagination directory
  final pageDirEntity = Directory(pageDir);
  if (await pageDirEntity.exists()) {
    print('🗑️  Cleaning up existing pagination files...');
    await pageDirEntity.delete(recursive: true);
  }

  // Create pagination files (page 2 onwards, page 1 is index.md)
  for (int pageNum = 2; pageNum <= totalPages; pageNum++) {
    await _createPaginationFile(pageNum, totalPages, pageDir);
  }

  print('✅ Generated ${totalPages - 1} pagination files');
  print('🎉 Home page pagination setup complete!');
  print('');
  print('📍 File structure:');
  print('   /           → Page 1 (content/index.md)');
  for (int i = 2; i <= totalPages; i++) {
    print('   /page/$i → Page $i (content/page/$i/index.md)');
  }
}

/// Create a pagination markdown file for a specific page number
Future<void> _createPaginationFile(
  int pageNum,
  int totalPages,
  String baseDir,
) async {
  final dir = Directory('$baseDir/$pageNum');
  await dir.create(recursive: true);

  final file = File('${dir.path}/index.md');

  // Generate frontmatter
  final content =
      '''---
layout: flexible
page_num: $pageNum
page-type: home
---
<HomePage />

<!-- This page is auto-generated by bin/generate_home_pagination.dart -->
<!-- It displays all blog posts (page $pageNum of $totalPages) -->
''';

  await file.writeAsString(content);
  print('   ✓ Created: ${file.path}');
}
