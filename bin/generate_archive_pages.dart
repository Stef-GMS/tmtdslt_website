import 'dart:io';

import 'package:path/path.dart' as p;

// Import the constant for posts per page
const int postsPerPage = 10;

/// Generates archive index pages for each month that has posts.
///
/// This script scans content/posts/ for year/month folders and creates
/// index.md files at content/posts/YYYY/YYYY-MM/index.md
///
/// These index files use the ArchivePage component to display posts
/// for that specific month.
///
/// Run this script:
/// - After migrating new posts
/// - When adding posts to existing months
/// - To regenerate all archive pages
void main() async {
  print('🗂️  Generating archive index pages...\n');

  final postsDir = Directory('content/posts');
  if (!postsDir.existsSync()) {
    print('❌ content/posts/ directory not found!');
    exit(1);
  }

  int created = 0;
  int skipped = 0;

  // Find all year directories
  final yearDirs =
      postsDir.listSync().whereType<Directory>().where((d) => RegExp(r'^\d{4}$').hasMatch(p.basename(d.path))).toList()
        ..sort((a, b) => p.basename(a.path).compareTo(p.basename(b.path)));

  for (final yearDir in yearDirs) {
    final year = p.basename(yearDir.path);

    // Find all month directories in this year
    final monthDirs =
        yearDir
            .listSync()
            .whereType<Directory>()
            .where((d) => RegExp(r'^\d{4}-\d{2}$').hasMatch(p.basename(d.path)))
            .toList()
          ..sort((a, b) => p.basename(a.path).compareTo(p.basename(b.path)));

    for (final monthDir in monthDirs) {
      final month = p.basename(monthDir.path);

      // Count posts in this month (exclude index.md files)
      final postCount = monthDir
          .listSync()
          .whereType<File>()
          .where((f) => f.path.endsWith('.md') && !f.path.endsWith('index.md'))
          .length;

      if (postCount == 0) {
        print('⏭️  Skipping $month (no posts)');
        skipped++;
        continue;
      }

      // Calculate total pages needed
      final totalPages = (postCount / postsPerPage).ceil();

      // Create index.md for page 1 (main archive page)
      final indexFile = File(p.join(monthDir.path, 'index.md'));
      final frontmatter = _generateFrontmatter(year, month, postCount, 1);
      await indexFile.writeAsString(frontmatter);

      // Create additional pages if needed
      if (totalPages > 1) {
        for (var pageNum = 2; pageNum <= totalPages; pageNum++) {
          // Create page directory: posts/YYYY/YYYY-MM/page/N/
          final pageDir = Directory(p.join(monthDir.path, 'page', pageNum.toString()));
          await pageDir.create(recursive: true);

          // Create index.md in the page directory
          final pageIndexFile = File(p.join(pageDir.path, 'index.md'));
          final pageFrontmatter = _generateFrontmatter(year, month, postCount, pageNum);
          await pageIndexFile.writeAsString(pageFrontmatter);
        }
        print('✅ Created: /posts/$year/$month/ ($postCount posts, $totalPages pages)');
      } else {
        print('✅ Created: /posts/$year/$month/ ($postCount posts)');
      }

      created++;
    }
  }

  print('\n📊 Summary:');
  print('   ✅ Created: $created archive pages');
  print('   ⏭️  Skipped: $skipped empty months');
  print('\n🎉 Done! Archive pages are ready.');
}

/// Generate frontmatter and content for archive index page
String _generateFrontmatter(String year, String month, int postCount, int pageNum) {
  final monthName = _getMonthName(month);

  return '''---
layout: flexible
year: $year
month: $month
page_num: $pageNum
page-type: archive
---
<ArchivePage />

<!-- This page is auto-generated by bin/generate_archive_pages.dart -->
<!-- It displays posts from $monthName (page $pageNum of ${(postCount / postsPerPage).ceil()}) -->
<!-- Title removed - ArchivePage renders header with inline dropdown -->
''';
}

/// Convert YYYY-MM format to "Month YYYY" format
String _getMonthName(String yearMonth) {
  try {
    final parts = yearMonth.split('-');
    if (parts.length != 2) return yearMonth;

    final year = parts[0];
    final monthNum = int.parse(parts[1]);

    const monthNames = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December',
    ];

    if (monthNum < 1 || monthNum > 12) return yearMonth;

    return '${monthNames[monthNum - 1]} $year';
  } catch (e) {
    return yearMonth;
  }
}
